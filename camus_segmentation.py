def preprocess(path, is_mask=False, target_size=(320, 320)):
    """
    Pré-processa imagem ou máscara para treinamento com PyTorch.
    - Lê com SimpleITK
    - Redimensiona
    - Normaliza se for imagem
    - Retorna tensor com shape [1, H, W] para imagem e [H, W] para máscara
    """
    import SimpleITK as sitk
    import numpy as np
    import cv2
    import skimage.transform

    # Lê a imagem com SimpleITK
    data = sitk.GetArrayFromImage(sitk.ReadImage(path, sitk.sitkFloat32))

    # Usa o primeiro slice se for 3D
    if data.ndim == 3:
        data = data[0]

    # Redimensionamento
    if is_mask:
        # Resize com nearest-neighbor (mantém valores inteiros)
        data_resized = skimage.transform.resize(
            data, target_size, order=0, preserve_range=True, anti_aliasing=False
        ).astype(np.uint8)  # convertendo para inteiro (valores de classe)
        return torch.from_numpy(data_resized).long()  # shape: [H, W]
    else:
        # Resize com interpolação bicúbica
        data_resized = cv2.resize(data, target_size, interpolation=cv2.INTER_CUBIC)
        data_resized = data_resized / 255.0  # Normaliza imagem
        data_tensor = np.expand_dims(data_resized, axis=0)  # shape: [1, H, W]
        return torch.from_numpy(data_tensor).float()

