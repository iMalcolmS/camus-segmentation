    overlay = frame.copy()
    for class_idx, color in class_colors.items():
        class_mask = (mask_np == class_idx).astype(np.uint8)
        overlay[class_mask == 1] = (
            overlay[class_mask == 1] * (1 - alpha) + np.array(color) * alpha
        ).astype(np.uint8)
    return overlay

