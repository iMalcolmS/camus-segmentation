alpha = 0.5       # Transparência da máscara (0.5 = 50% de opacidade)

# ✅ Cores RGBA para cada classe (Vermelho, Verde, Azul)
class_colors = {
    1: (255, 255, 0, int(255 * alpha)),   # Classe 1: Amarelo
    2: (0, 0, 255, int(255 * alpha)),     # Classe 2: Azul
    3: (255, 0, 255, int(255 * alpha))    # Classe 3: Azul
}

# ✅ Função para sobrepor máscaras
def overlay_mask(base_frame, mask_np):
    # Converter a máscara para valores de classe originais (0, 1, 2, 3)
    mask_np = (mask_np // (255 // 3)).astype(np.uint8)  # Reverte a normalização

    # Criar uma imagem RGBA para a máscara
    overlay = np.zeros((*mask_np.shape, 4), dtype=np.uint8)

    for class_idx in [1, 2, 3]:
        overlay[mask_np == class_idx] = class_colors[class_idx]

    # Combinar com o frame original
    base_frame_rgba = cv2.cvtColor(base_frame, cv2.COLOR_RGB2RGBA)
    combined = cv2.addWeighted(base_frame_rgba, 1, overlay, 1, 0)

    return combined

