# Função para calcular o IoU
def calculate_iou(preds, targets, num_classes):
    iou_per_class = []
    preds = preds.view(-1)
    targets = targets.view(-1)

    for cls in range(num_classes):
        pred_inds = preds == cls
        target_inds = targets == cls

        intersection = (pred_inds & target_inds).sum().item()
        union = (pred_inds | target_inds).sum().item()

        if union == 0:
            iou = np.nan  # Se não houver pixel da classe
        else:
            iou = intersection / union
        iou_per_class.append(iou)

    return iou_per_class

# Função para calcular o Dice
def calculate_dice(preds, targets, num_classes):
    dice_per_class = []
    preds = preds.view(-1)
    targets = targets.view(-1)

    for cls in range(num_classes):
        pred_inds = preds == cls
        target_inds = targets == cls

        intersection = (pred_inds & target_inds).sum().item()
        dice = (2. * intersection) / (pred_inds.sum().item() + target_inds.sum().item() + 1e-8)

        dice_per_class.append(dice)

    return dice_per_class

# Função para calcular a acurácia por classe
def calculate_accuracy_per_class(preds, targets, num_classes):
    accuracy_per_class = []
    preds = preds.view(-1)
    targets = targets.view(-1)

    for cls in range(num_classes):
        pred_inds = preds == cls
        target_inds = targets == cls

        correct = (pred_inds & target_inds).sum().item()
        total = target_inds.sum().item()

        if total == 0:
            accuracy = np.nan
        else:
            accuracy = correct / total

        accuracy_per_class.append(accuracy)

    return accuracy_per_class

# Função para calcular a matriz de confusão
def calculate_confusion_matrix(preds, targets, num_classes):
    preds = preds.view(-1).cpu().numpy()  # Garantir que esteja na CPU para compatibilidade
    targets = targets.view(-1).cpu().numpy()  # Garantir que esteja na CPU para compatibilidade

    cm = confusion_matrix(targets, preds, labels=list(range(num_classes)))
    return cm

# Função para calcular precisão, recall e F1
def calculate_precision_recall_f1(preds, targets, num_classes):
    preds = preds.view(-1).cpu().numpy()  # Garantir que esteja na CPU para compatibilidade
    targets = targets.view(-1).cpu().numpy()  # Garantir que esteja na CPU para compatibilidade

    precision = precision_score(targets, preds, labels=list(range(num_classes)), average=None, zero_division=0)
    recall = recall_score(targets, preds, labels=list(range(num_classes)), average=None, zero_division=0)
    f1 = f1_score(targets, preds, labels=list(range(num_classes)), average=None, zero_division=0)

    return precision, recall, f1

